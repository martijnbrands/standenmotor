     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $match = request("match");
        $match = Match::find($match["id"]);

        $players = Player::find(request('players'));

        $match->players()->sync($players);

        return [
            "match" => $match,
            "players" => $players
        ];
    }

